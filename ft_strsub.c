#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*str;

	if (!s)
		return (NULL);
	if ((str = ft_strnew(len)) == NULL)
		return (NULL);
	ft_memcpy(str, &s[start], len);
	return (str);
}
